      SUBROUTINE fvecjac(ap, N, fvec, fjac, T, atcon, atom, mol)
      use PARAMS_ARRAY
     
      IMPLICIT NONE 
      integer nn
      PARAMETER (NN=4)

      CHARACTER*2 atom(4), mol(4,4)
      CHARACTER*2 SYMBOL(MAXATOM), molec
      INTEGER N, i, j, l

      real*8 ap(4), atcon(4)
      real*8 fvec(4), fjac(4,4)
      real*8  Kn, T, sum
    

 

!     Values at ap    

      do i=1, N
      sum=0
       do j=1, N
        if (i .eq. j) then 
        
        molec=trim(atom(i))//'2'
        call Kmol(T, molec, Kn)
        sum=sum+2.*ap(i)/Kn
        
        else
        
        molec=trim(atom(i))//trim(atom(j))
        call Kmol(T, molec, Kn)
        sum=sum+ap(j)/Kn

        endif

       enddo   
      fvec(i)=(sum+1.)*ap(i)-atcon(i) 
      enddo


!     Jacobian matrix at ap


      do i=1, N
       do j=1, N
       fjac(i,j)=0.
        if (i .eq. j) then         
               do l=1, N
                 if (l .eq. i) then
                 molec=trim(atom(i))//'2'
                 call Kmol(T, molec, Kn)
                 fjac(i,j)=fjac(i,j)+4.*ap(i)/Kn
                 else
                 molec=trim(atom(i))//trim(atom(l))
                 call Kmol(T, molec, Kn)
                 fjac(i,j)=fjac(i,j)+ap(l)/Kn
                 endif
               enddo  
               fjac(i,j)=fjac(i,j)+1.
        else
        molec=trim(atom(i))//trim(atom(j))
        call Kmol(T, molec, Kn)
        fjac(i,j)=ap(i)/Kn
        endif
       enddo
      enddo
    


      end subroutine




      
