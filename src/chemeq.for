      SUBROUTINE mnewt(ntrial,x,n,tolx,tolf, T_nl, atcon, atom, mol)
!      use MOD_chemeq
      INTEGER n,NN, ntrial,NP
      REAL*8 tolf,tolx
      PARAMETER (NP=4)
      PARAMETER (NN=4)
CU    USES lubksb,ludcmp,usrfun
      INTEGER i,k,indx(4)

      real*8 fjac(4,4), fvec(4), atcon(4), x(4)

      CHARACTER*2 atom(4), mol(4,4)
      REAL*8 d,errf,errx,T_nl,p(4)
      do 14  k=1,ntrial
!        print *, k, x(1)
        call fvecjac(x,n,fvec,fjac,  T_nl, atcon, atom, mol)
        errf=0.
        do 11 i=1,n
          errf=errf+abs(fvec(i))
11      continue
!        print *, errf
        if(errf.le.tolf)return
        do 12 i=1,n
          p(i)=-fvec(i)
12      continue
        call ludcmp(fjac,n,NP,indx,d)
        call lubksb(fjac,n,NP,indx,p)

        errx=0.
        do 13 i=1,n
          errx=errx+abs(p(i))
          x(i)=x(i)+p(i)
13      continue
        if(errx.le.tolx)return
14    continue
      return
      end subroutine




      SUBROUTINE lubksb(a,n,np,indx,b)
      INTEGER n,np,indx(4)
      REAL*8 b(4)
      INTEGER i,ii,j,ll
      REAL*8 sum
!      real*8, allocatable :: a(:,:)
      real*8 a(4,4)
      ii=0
      do 12 i=1,n
        ll=indx(i)
        sum=b(ll)
        b(ll)=b(i)
        if (ii.ne.0)then
          do 11 j=ii,i-1
            sum=sum-a(i,j)*b(j)
11        continue
        else if (sum.ne.0.) then
          ii=i
        endif
        b(i)=sum
12    continue
      do 14 i=n,1,-1
        sum=b(i)
        do 13 j=i+1,n
          sum=sum-a(i,j)*b(j)
13      continue
        b(i)=sum/a(i,i)
14    continue
      return

      end subroutine


      SUBROUTINE ludcmp(a,n,np,indx,d)
      INTEGER n,np,indx(4),NMAX
      REAL*8 d,TINY
 !     real*8, allocatable :: a(:,:)
      real*8 a(4,4)
      PARAMETER (NMAX=500,TINY=1.0e-20)
      INTEGER i,imax,j,k
      REAL*8 aamax,dum,sum,vv(NMAX)
      d=1.
      do 12 i=1,n
        aamax=0.
        do 11 j=1,n
          if (abs(a(i,j)).gt.aamax) aamax=abs(a(i,j))
11      continue
        if (aamax.eq.0.) pause 'singular matrix in ludcmp'
        vv(i)=1./aamax
12    continue
      do 19 j=1,n
        do 14 i=1,j-1
          sum=a(i,j)
          do 13 k=1,i-1
            sum=sum-a(i,k)*a(k,j)
13        continue
          a(i,j)=sum
14      continue
        aamax=0.

        do 16 i=j,n
          sum=a(i,j)
          do 15 k=1,j-1
            sum=sum-a(i,k)*a(k,j)
15        continue
          a(i,j)=sum
          dum=vv(i)*abs(sum)
          if (dum.ge.aamax) then
            imax=i
            aamax=dum
          endif
16      continue
        if (j.ne.imax)then
          do 17 k=1,n
            dum=a(imax,k)
            a(imax,k)=a(j,k)
            a(j,k)=dum
17        continue
          d=-d
          vv(imax)=vv(j)
        endif
        indx(j)=imax
        if(a(j,j).eq.0.)a(j,j)=TINY

        if(j.ne.n)then
          dum=1./a(j,j)
          do 18 i=j+1,n
            a(i,j)=a(i,j)*dum
18        continue
        endif
19    continue
      return
      end subroutine


     
