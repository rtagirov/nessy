     *             'O2',  5.1156/ 

     
      number=0

      inverse=.false.

      molh=mol
      
 10   DO i=1, Nm 
   
      if (molh .eq. FC(i)%name) then
      number=i
      endif

      ENDDO


      if (number .eq. 0.) then
      molh=mol(2:2)//mol(1:1)
      inverse=.true.
      goto 10
      endif

      if ((number .eq. 0.) .and. inverse) then
      stop 'chemeq.for: wrong molecule'
      endif

      
      phiT=log10(5040./T)
      phi(1)=1.
      phi(2)=phiT
